function RenderOptions (){
    this.reqData = new FormData();
    this.reqData.append('act','srch');

    this.getOffices = async(selector) =>{
        this.officesdata = await sendHttpRequest('eaucnmanage/managecommonoffice',this.reqData);
        if(selector)
        document.querySelectorAll(selector).forEach(element => {
            element.innerHTML = getOptions(this.officesdata.Records,'OFC_NM','OFCID',"Main Office","0");
        });
        
    }

    this.getDesignations = async (selector) =>{
        this.designationData = await sendHttpRequest('eaucnmanage/managecommondesigs',this.reqData);
        if(selector)
        document.querySelectorAll(selector).forEach(element => {
            element.innerHTML = getOptions(this.designationData.Records,'DSG_NM','DSGID',"Office Authority","0");
        });
    }

    this.getUserRole = async (selector) =>{
        this.reqData.delete('act');
        this.reqData.append('act','getAppRoles');
        this.roleData = await sendHttpRequest('eaucnmanage/manageusers',this.reqData);
        if(selector)
        document.querySelectorAll(selector).forEach(element => {
            element.innerHTML = getOptions(this.roleData.Records,'ronm','roid',"STAD","0"); 
        });
    }


}
